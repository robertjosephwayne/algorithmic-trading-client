import cn from 'classnames';

export default function Candlestick(props: any) {
    const {
        x,
        y,
        width,
        height,
        low,
        high,
        openClose: [open, close],
        showHighLow,
    } = props;
    const isGrowing = open < close;
    const color = isGrowing ? 'green' : 'red';
    const ratio = Math.abs(height / (open - close));

    return (
        <g stroke={color} fill='black' strokeWidth='2'>
            <path
                d={`
          M ${x},${y}
          L ${x},${y + height}
          L ${x + width},${y + height}
          L ${x + width},${y}
          L ${x},${y}
        `}
            />
            {/* bottom line */}
            {isGrowing ? (
                <path
                    className={cn({ 'opacity-0': !showHighLow })}
                    d={`
            M ${x + width / 2}, ${y + height}
            v ${(open - low) * ratio}
          `}
                />
            ) : (
                <path
                    className={cn({ 'opacity-0': !showHighLow })}
                    d={`
            M ${x + width / 2}, ${y}
            v ${(close - low) * ratio}
          `}
                />
            )}
            {/* top line */}
            {isGrowing ? (
                <path
                    className={cn({ 'opacity-0': !showHighLow })}
                    d={`
            M ${x + width / 2}, ${y}
            v ${(close - high) * ratio}
          `}
                />
            ) : (
                <path
                    className={cn({ 'opacity-0': !showHighLow })}
                    d={`
            M ${x + width / 2}, ${y + height}
            v ${(open - high) * ratio}
          `}
                />
            )}
        </g>
    );
}
